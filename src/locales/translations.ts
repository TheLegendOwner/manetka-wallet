// Type for nested object keys with dot notation
type NestedKeyOf<ObjectType extends object> = {
  [Key in keyof ObjectType & (string | number)]: ObjectType[Key] extends object
    ? `${Key}` | `${Key}.${NestedKeyOf<ObjectType[Key]>}`
    : `${Key}`;
}[keyof ObjectType & (string | number)];

type TranslationKey = NestedKeyOf<typeof translations.en>;

// Helper function to get nested value by dot notation
function getNestedValue<T>(obj: T, path: string): string {
  return path.split('.').reduce((current: any, key: string) => {
    return current?.[key];
  }, obj) || path;
}

// Translation function
export function useTranslation(language: Language) {
  return function t(key: TranslationKey): string {
    const translationObj = translations[language];
    return getNestedValue(translationObj, key);
  };
}

// Additional helper for direct translation without hook
export function getTranslation(language: Language, key: TranslationKey): string {
  const translationObj = translations[language];
  return getNestedValue(translationObj, key);
}